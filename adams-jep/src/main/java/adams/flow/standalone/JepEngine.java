public class JepEngine
  extends AbstractStandalone {

  private static final long serialVersionUID = -7099840275220933228L;

  /** the engine. */
  protected JepScriptingEngine m_Engine;

  /**
   * Returns a string describing the object.
   *
   * @return a description suitable for displaying in the gui
   */
  @Override
  public String globalInfo() {
    return "Provides a local context for executing Jep/Python scripts in parallel.\n"
	     + "However, the user needs to take care that the variables of parallel executed scripts "
	     + "do not interfere with each other.";
  }

  /**
   * Returns the Jep scripting engine instance for the local context.
   *
   * @return		the engine, null if not available
   */
  public JepScriptingEngine getEngine() {
    return m_Engine;
  }

  /**
   * Executes the flow item.
   *
   * @return null if everything is fine, otherwise error message
   */
  @Override
  protected String doExecute() {
    m_Engine = new JepScriptingEngine();
    return null;
  }

  /**
   * Cleans up after the execution has finished. Graphical output is left
   * untouched.
   */
  @Override
  public void wrapUp() {
    if (m_Engine != null) {
      m_Engine.stopEngine();
      m_Engine = null;
    }
    super.wrapUp();
  }
}
